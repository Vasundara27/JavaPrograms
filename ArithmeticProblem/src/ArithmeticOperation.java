
public class ArithmeticOperation {
	public void Add(int a,int b)
	{
		System.out.println("Addition of 2 numbers:"+(a+b));
	}
	public void Sub(int m,int n)
	{
		System.out.println("Substraction of 2 numbers:"+(m-n));
	}
	public void Mul(int b,int a)
	{
		System.out.println("Multiplication of 2 numbers:"+(a*b));
	}
	public void Div(int a,int b)
	{
		System.out.println("Division of 2 numbers:"+(a/b));
	}
	public void Reminder(int a,int b)
	{
		System.out.println("Reminder of 2 numbers:"+(a%b));
	}





}
